import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const outDir = path.join(root, 'src', 'generated');
const outFile = path.join(outDir, 'appVersion.ts');

function resolveBuildVersion() {
  try {
    return execSync('git rev-list --count HEAD', { encoding: 'utf8' }).trim();
  } catch {
    return '0';
  }
}

const version = resolveBuildVersion();
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  outFile,
  `// Auto-generated by scripts/gen-app-version.mjs — do not edit.\nexport const APP_BUILD_VERSION = ${JSON.stringify(version)};\n`,
  'utf8',
);
console.log(`gen-app-version: ${version}`);
